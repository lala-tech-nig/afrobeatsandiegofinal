const mongoose = require('mongoose');

const bookCallSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true,
        lowercase: true
    },
    phoneNumber: {
        type: String,
        required: true,
        trim: true
    },
    message: {
        type: String,
        required: true
    },
    status: {
        type: String,
        enum: ['new', 'read', 'contacted'],
        default: 'new'
    }
}, {
    timestamps: true
});

module.exports = mongoose.model('BookCall', bookCallSchema);
